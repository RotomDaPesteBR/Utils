/// </summary>
public enum OrderDirection
{
    /// <summary>
    /// Sorts in ascending order (e.g., A to Z, 1 to 10).
    /// </summary>
    Ascending,

    /// <summary>
    /// Sorts in descending order (e.g., Z to A, 10 to 1).
    /// </summary>
    Descending
}
